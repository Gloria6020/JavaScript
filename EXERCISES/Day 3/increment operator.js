/*In JavaScript we use the increment operator to increase a value stored in a variable.
 The increment could be pre or post increment.*/

 //pre-increment
 let count = 0
console.log(++count)        // 1
console.log(count)          // 1

//post-increment
let count2 = 0
console.log(count2++)        // 0
console.log(count2)          // 1